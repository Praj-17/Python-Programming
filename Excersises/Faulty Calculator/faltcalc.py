
operator = input()
n1 = int(input()); n2 = int(input())
if operator == "+":
    
    if n1 ==56 and n2 == 9:
        print(77)
    else:
        print(n1+n2)
elif operator == "-":
    print(n1-n2)
elif operator == "*":
    
    if n1 ==45 and n2 == 3:
        print(555)
    else:
        print(n1*n2)
if operator == "/":
    
    if n1 ==56 and n2 == 6:
        print(4)
    else:
        print(n1/n2)
